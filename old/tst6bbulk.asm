mkcheck = *
    txa
    sta cc,x ; initialize the check code
    lda #D1BASE>>8
    sta bk
    jsr bkcheck
    lda #D2BASE>>8
    sta bk
    jsr bkcheck
    lda #D3BASE>>8
    sta bk
    jsr bkcheck
    lda #D4BASE>>8
    sta bk
    jsr bkcheck
    lda #D5BASE>>8
    sta bk
    jsr bkcheck
    lda #D6BASE>>8
    sta bk
    jsr bkcheck
    rts

    ; bkcheck - update a byte of check code (pointed to by X) with 4kB
    ; of data starting at address (bk)*256
bkcheck = *
    lda bk
    sta pp+1
    lda #0
    sta pp
    jsr pgcheck
    inc pp+1
    jsr pgcheck
    inc pp+1
    jsr pgcheck
    inc pp+1
    jsr pgcheck
    inc pp+1
    jsr pgcheck
    inc pp+1
    jsr pgcheck
    inc pp+1
    jsr pgcheck
    inc pp+1
    jsr pgcheck
    inc pp+1
    jsr pgcheck
    inc pp+1
    jsr pgcheck
    inc pp+1
    jsr pgcheck
    inc pp+1
    jsr pgcheck
    inc pp+1
    jsr pgcheck
    inc pp+1
    jsr pgcheck
    inc pp+1
    jsr pgcheck
    inc pp+1
    jsr pgcheck
    rts

    ; pgcheck - update a byte of check code (pointed to by X) with 256
    ; bytes of data starting at address (pp)
pgcheck = *
    ldy #0 ; index & byte counter
pgcheck__loop = *
    lda (pp),y ; get the next byte
    clc
    adc cc,x ; this is twos complement addition
    adc #0 ; making it ones complement by putting carry back in
    sta cc,x ; storing the result temporary
    ror a ; and rotating right one bit
    ror cc,x
    iny ; and going on to the next data byte
    bne pgcheck__loop ; if there are any
    rts ; if not, done
    
